package vo;

public class MemberPointVO {
	private String artist_name;
	private String sumpoint;
	private String roundpoint;
	private String ranknum;
	
	
	public String getArtist_name() {
		return artist_name;
	}
	public void setArtist_name(String artist_name) {
		this.artist_name = artist_name;
	}
	public String getSumpoint() {
		return sumpoint;
	}
	public void setSumpoint(String sumpoint) {
		this.sumpoint = sumpoint;
	}
	public String getRoundpoint() {
		return roundpoint;
	}
	public void setRoundpoint(String roundpoint) {
		this.roundpoint = roundpoint;
	}
	public String getRanknum() {
		return ranknum;
	}
	public void setRanknum(String ranknum) {
		this.ranknum = ranknum;
	}
	
}
